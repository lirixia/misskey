export function applyFont(fontname: null | string) {
	let style = window.document.getElementById('custom-font');

	if (!fontname) {
		if (!style) return;
		return style.remove();
	}

	if (!style) {
		style = window.document.createElement('style');
		style.id = 'custom-font';
		window.document.head.appendChild(style);
	}

	const font = fontList[fontname];
	if (!font) return;

	if (font.custom) {
		if (font.isCSS) {
			// CSSファイルをインポートする場合
			style.innerHTML = `
				@import url('${font.importUrl}');
				body {
					font-family: '${font.fontFamily}', 'Hiragino Maru Gothic Pro', 'BIZ UDGothic', Roboto, HelveticaNeue, Arial, sans-serif !important;
					font-style: normal;
				}
			`;
		} else {
			// フォントファイルを直接使用する場合
			style.innerHTML = `
				@font-face {
					font-family: '${font.fontFamily}';
					src: url('${font.importUrl}');
				}
				body {
					font-family: '${font.fontFamily}', 'Hiragino Maru Gothic Pro', 'BIZ UDGothic', Roboto, HelveticaNeue, Arial, sans-serif !important;
					font-style: normal;
				}
			`;
		}
	} else {
		style.innerHTML = `
			@import url('${font.importUrl}');
			body {
				font-family: '${font.fontFamily}', 'Hiragino Maru Gothic Pro', 'BIZ UDGothic', Roboto, HelveticaNeue, Arial, sans-serif !important;
				font-style: normal;
			}
		`;
	}
}
